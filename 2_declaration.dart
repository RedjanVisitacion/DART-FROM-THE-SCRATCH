void main() {
  // Numbers: int
  int age = 19;

  // Numbers: double
  double grade = 2.25;

  // Strings
  String subject = "Rizal";

  // Boolean
  bool status = true;

  // Prints the output
  print("Subject: " + subject + ":");
  print("Grade: " + grade.toString());

  print("Single: " + status.toString());
  print("Age is: " + age.toString());
}
