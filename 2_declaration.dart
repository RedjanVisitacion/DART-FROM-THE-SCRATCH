void main() {
  // Numbers: int
  // Declares an integer variable to store whole number values
  int age = 19;

  // Numbers: double
  // Declares a double variable to store decimal values
  double grade = 2.25;

  // Strings
  // Declares a string variable to store text
  String subject = "Rizal";

  // Boolean
  // Declares a boolean variable to store true or false values
  bool status = true;

  // Prints the output
  // Displaying each variable with a label using string concatenation
  print("Subject: " + subject + ":"); // Output: Subject: Rizal:
  print("Grade: " + grade.toString()); // Output: Grade: 2.25
  print("Single: " + status.toString()); // Output: Single: true
  print("Age is: " + age.toString()); // Output: Age is: 19
}
