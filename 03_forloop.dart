void main() {
  // A for loop that runs from 1 to 5
  for (int redjan = 1; redjan <= 5; redjan++) {
    // Converts the integer to string and prints a message for each iteration
    print("[" + redjan.toString() + "]. This is forloop");
    // Example output:
    // [1]. This is forloop
    // [2]. This is forloop
    // [3]. This is forloop
    // [4]. This is forloop
    // [5]. This is forloop
  }
}
